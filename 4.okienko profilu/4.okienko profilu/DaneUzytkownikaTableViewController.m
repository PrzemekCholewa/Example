- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    CustomTableViewCell *cell = (CustomTableViewCell *)[tableView dequeueReusableCellWithIdentifier:@"cellID"];
    
    
    switch (indexPath.row){
        case 0: cell.Label.text = @"Imię"; break;
        case 1: cell.Label.text = @"Nazwisko (opcjonalne)"; break;
        case 2: cell.Label.text = @"Stare hasło"; break;
        case 3: cell.Label.text = @"Nowe hasło"; break;
        default: cell.Label.text = @"Whaaat?"; break;
    }
    return cell;
}

